
public class Size {

	// Instance variables
	private int width, height;

	public Size(int width, int height) { // Size class, created from width and
											// height
		this.width = width;
		this.height = height;
	}

	// accessors
	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}
}
